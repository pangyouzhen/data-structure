from typing import List
from itertools import combinations

class Solution:
    def canPartition(self, nums: List[int]) -> bool:
        s = sum(nums)
        if s % 2 != 0:
            return False
        n = len(nums)
        t = s / 2
        for i in range(n):
            for j in combinations(nums, i):
                if sum(list(j)) == t:
                    return True
        return False


